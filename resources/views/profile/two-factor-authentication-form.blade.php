            </p>
        </div>

        @if ($this->enabled)
            @if ($showingQrCode)
                <div class="mt-4 max-w-xl text-sm text-gray-600 dark:text-gray-400">
                    <p class="font-semibold">
                        @if ($showingConfirmation)
                            {{ __('За да завършите активирането на двуфакторно удостоверяване, сканирайте следния QR код, като използвате приложението за удостоверяване на телефона си или въведете ключа за настройка и предоставете генерирания OTP код.') }}
